package it.polito.tdp.porto.model;

public class AuthorPair {
	
	private int Author1;
	private int Author2;
	
	
	public AuthorPair(int author1, int author2) {
		super();
		Author1 = author1;
		Author2 = author2;
	}


	/**
	 * @return the author1
	 */
	public int getAuthor1() {
		return Author1;
	}


	/**
	 * @return the author2
	 */
	public int getAuthor2() {
		return Author2;
	}
	
	


}
